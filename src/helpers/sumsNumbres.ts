
export const sumTwoNumbres = ( a: number, b: number ): Number => {
    return a + b;
}

export const sumThreeNumbres = ( a: number, b: number, c: number ): Number => {
    return a + b + c;
}

export const sumFourNumbres = ( a: number, b: number, c: number, d: number ): Number => {
    return a + b + c + d;
}